# In Ruby, there are different *types* of data:

# Numbers
# Put the number 5 on screen
# puts 5

# Put the sum of 2 and 5 on screen
# puts 2 + 5

# Non-numbers
# Put my favorite thing on the screen
# puts "tacos"

# True, False
# puts true
# puts false

# Nothing
# puts nil

# Variables
# Store the number 5 in the computer's memory
# x = 5

# Show the value contained at x on screen
# puts x

# Replace the value of x with 6
# x = x + 1

# Show the new value
# puts x

# Combine strings and variables
# Store my favorite thing in memory
my_favorite_thing = "tacos"

# Store a quantity in memory
number_of_things = 2

# Display the two together
puts "my order is... #{number_of_things} #{my_favorite_thing}" 

